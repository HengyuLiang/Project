package Calendar;

public class CalendarData {

	private int year;
	private int month;
	
	public int getYear(){
		return year;
	}
	public void setYear(int year){
		this.year=year;
	}
	
	/*
	 * Month value starts from 0 to 11.
	 */
	public int getMonth(){
		return month;
	}
	
	public void setMonth(){
		this.month=month;
	}
	
	
}
